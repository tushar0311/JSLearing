// index++==> index=index+1
// index--==> index=index-1


// post increment
var index=10;
var y =index++;
console.log(index,y);


// post decrement
var index=10;
var y =index--;
console.log(index,y);

// pre increment
var index=10;
var y =++index;   
console.log(index,y);

// pre increment
var index=10;
var y =--index;
console.log(index,y);